def main():
    # Validation
    with open(QUESTIONS_FILE, "r") as f:
        data = json.load(f)
    existing_ids = set(q["id"] for q in data["questions"])
    
    errors = []
    for q in NEW_QUESTIONS:
        if q["id"] in existing_ids:
            errors.append(f"Duplicate ID: {q['id']}")
        if len(q["opts"]) != 4:
            errors.append(f"{q['id']}: opts not 4 ({len(q['opts'])})")
        if len(set(q["opts"])) != 4:
            errors.append(f"{q['id']}: opts have duplicates")
        if not (0 <= q["a"] <= 3):
            errors.append(f"{q['id']}: a out of range ({q['a']})")
        if q["a"] not in range(4):
            errors.append(f"{q['id']}: a invalid")
    
    if errors:
        print("VALIDATION ERRORS:")
        for e in errors:
            print(f"  {e}")
        sys.exit(1)
    
    # Append (only via main)
    if "--append" in sys.argv:
        data["questions"].extend(NEW_QUESTIONS)
        data["total"] = len(data["questions"])
        data["version"] = data.get("version", 1) + 1
        with open(QUESTIONS_FILE, "w") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"Appended {len(NEW_QUESTIONS)} questions. New total: {data['total']}, version: {data['version']}")
    else:
        print(f"Validation OK. {len(NEW_QUESTIONS)} questions ready to append.")
        for q in NEW_QUESTIONS[:5]:
            print(f"  Sample: {q}")


if __name__ == "__main__":
    main()